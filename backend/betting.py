# ----------------------------- Helpers -----------------------------
async def ensure_user_points(user_id: str) -> int:
    u = await db.users.find_one({"id": user_id}, {"points": 1})
    if u is None:
        return 0
    if "points" not in u:
        await db.users.update_one({"id": user_id}, {"$set": {"points": DEFAULT_STARTING_POINTS}})
        return DEFAULT_STARTING_POINTS
    return int(u.get("points") or 0)


async def get_match_odds(match_id: str) -> Dict[str, float]:
    m = await db.matches.find_one({"id": match_id}, {"odds": 1, "bet_locks": 1})
    if not m:
        return dict(DEFAULT_ODDS)
    odds = m.get("odds") or {}
    result = dict(DEFAULT_ODDS)
    result.update({k: float(v) for k, v in odds.items() if k in ALL_ODDS_KEYS and isinstance(v, (int, float))})
    return result


def bet_type_of_selection(selection: str) -> Optional[str]:
    for bt, sels in SELECTIONS.items():
        if selection in sels:
            return bt
    return None


# ----------------------------- Groq Odds Generator -----------------------------
GROQ_URL = "https://api.groq.com/openai/v1/chat/completions"

SYSTEM_PROMPT_ODDS = (
    "You are a professional football (soccer) betting odds compiler for an eFootball amateur league. "
    "Given two teams' recent form and head-to-head data, produce realistic decimal odds. "
    "Rules:\n"
    "1) Return STRICT JSON only, no prose, no markdown fences.\n"
    "2) Fields (all required, decimal >= 1.10):\n"
    "   ms_1, ms_x, ms_2, goal_over_2_5, goal_under_2_5, corner_over_4_5, corner_under_4_5\n"
    "3) MS probabilities (1/odd) must sum roughly to 1.05-1.15 (bookmaker margin).\n"
    "4) Over/Under pairs must also sum roughly to 1.05-1.15 (margin).\n"
    "5) Cold-start rule: if a team has fewer than 5 matches OR no corner history, use balanced ~1.85/~1.85 for corners.\n"
    "6) Realistic ranges: MS between 1.30 and 8.00. O/U between 1.40 and 3.20.\n"
    "7) Round to two decimals."
)


async def generate_odds_via_groq(context: Dict[str, Any]) -> Dict[str, float]:
    api_key = os.getenv("GROQ_API_KEY", "").strip()
    model = os.getenv("GROQ_MODEL", "llama-3.3-70b-versatile").strip()
    if not api_key:
        logger.warning("GROQ_API_KEY missing; returning default odds")
        return dict(DEFAULT_ODDS)

    prompt = (
        "Return JSON odds for the upcoming match.\n"
        f"HOME_TEAM: {context.get('home_name')}\n"
        f"AWAY_TEAM: {context.get('away_name')}\n"
        f"HOME_LAST5: {json.dumps(context.get('home_last5', []))}\n"
        f"AWAY_LAST5: {json.dumps(context.get('away_last5', []))}\n"
        f"HEAD_TO_HEAD: {json.dumps(context.get('h2h', {}))}\n"
        f"HOME_MATCHES_PLAYED: {context.get('home_played', 0)}\n"
        f"AWAY_MATCHES_PLAYED: {context.get('away_played', 0)}\n"
        f"HOME_AVG_CORNERS: {context.get('home_avg_corners')}\n"
        f"AWAY_AVG_CORNERS: {context.get('away_avg_corners')}\n"
        f"LEAGUE_AVG_GOALS_PER_MATCH: {context.get('league_avg_goals', 2.7)}\n"
        f"LEAGUE_AVG_CORNERS_PER_MATCH: {context.get('league_avg_corners', 5.5)}\n"
    )

    payload = {
        "model": model,
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT_ODDS},
            {"role": "user", "content": prompt},
        ],
        "response_format": {"type": "json_object"},
        "temperature": 0.4,
        "max_tokens": 300,
    }
    headers = {"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"}
    try:
        async with httpx.AsyncClient(timeout=25.0) as client:
            r = await client.post(GROQ_URL, json=payload, headers=headers)
            r.raise_for_status()
            data = r.json()
            content = data["choices"][0]["message"]["content"]
            parsed = json.loads(content)
    except Exception as exc:
        logger.warning("Groq odds generation failed: %s", exc)
        return dict(DEFAULT_ODDS)

    out = dict(DEFAULT_ODDS)
    for k in ALL_ODDS_KEYS:
        v = parsed.get(k)
        try:
            fv = float(v)
            if 1.10 <= fv <= 20.0:
                out[k] = round(fv, 2)
        except (TypeError, ValueError):
            pass
    return out


async def _team_recent_matches(team_id: str, limit: int = 5) -> List[Dict[str, Any]]:
    cursor = db.matches.find(
        {"status": "finished",
         "$or": [{"home_team_id": team_id}, {"away_team_id": team_id}],
         "home_score": {"$ne": None}},
        {"_id": 0}
    ).sort([("finished_at", -1)]).limit(limit)
    out = []
    async for m in cursor:
        is_home = m["home_team_id"] == team_id
        gf = m.get("home_score", 0) if is_home else m.get("away_score", 0)
        ga = m.get("away_score", 0) if is_home else m.get("home_score", 0)
        cf = m.get("home_corners") if is_home else m.get("away_corners")
        ca = m.get("away_corners") if is_home else m.get("home_corners")
        out.append({
            "gf": gf, "ga": ga,
            "result": "W" if gf > ga else ("D" if gf == ga else "L"),
            "corners_for": cf, "corners_against": ca,
        })
    return out


async def _team_stats(team_id: str) -> Dict[str, Any]:
    matches = await db.matches.find(
        {"status": "finished",
         "$or": [{"home_team_id": team_id}, {"away_team_id": team_id}],
         "home_score": {"$ne": None}},
        {"_id": 0}
    ).to_list(2000)
    played = len(matches)
    corners = []
    for m in matches:
        is_home = m["home_team_id"] == team_id
        c = m.get("home_corners") if is_home else m.get("away_corners")
        if c is not None:
            corners.append(int(c))
    avg_corners = round(sum(corners) / len(corners), 2) if corners else None
    return {"played": played, "avg_corners": avg_corners}


async def _h2h_stats(a: str, b: str) -> Dict[str, Any]:
    matches = await db.matches.find(
        {"status": "finished",
         "$or": [
             {"home_team_id": a, "away_team_id": b},
             {"home_team_id": b, "away_team_id": a},
         ],
         "home_score": {"$ne": None}},
        {"_id": 0}
    ).to_list(500)
    wins_a = wins_b = draws = 0
    goals_a = goals_b = 0
    for m in matches:
        hs = int(m.get("home_score") or 0)
        as_ = int(m.get("away_score") or 0)
        if m["home_team_id"] == a:
            goals_a += hs; goals_b += as_
        else:
            goals_a += as_; goals_b += hs
        # winner
        if hs == as_:
            draws += 1
        else:
            winner = m["home_team_id"] if hs > as_ else m["away_team_id"]
            if winner == a: wins_a += 1
            else: wins_b += 1
    return {
        "total": len(matches),
        "wins_a": wins_a, "wins_b": wins_b, "draws": draws,
        "goals_a": goals_a, "goals_b": goals_b,
    }


async def build_odds_context(match: dict) -> Dict[str, Any]:
    home_team = await db.teams.find_one({"id": match["home_team_id"]}, {"name": 1})
    away_team = await db.teams.find_one({"id": match["away_team_id"]}, {"name": 1})
    home_last5 = await _team_recent_matches(match["home_team_id"], 5)
    away_last5 = await _team_recent_matches(match["away_team_id"], 5)
    home_stats = await _team_stats(match["home_team_id"])
    away_stats = await _team_stats(match["away_team_id"])
    h2h = await _h2h_stats(match["home_team_id"], match["away_team_id"])

    # League averages
    all_finished = await db.matches.find(
        {"status": "finished", "home_score": {"$ne": None}},
        {"_id": 0, "home_score": 1, "away_score": 1, "home_corners": 1, "away_corners": 1}
    ).to_list(5000)
    if all_finished:
        avg_goals = round(sum((m.get("home_score") or 0) + (m.get("away_score") or 0) for m in all_finished) / len(all_finished), 2)
        c_list = [(m.get("home_corners") or 0) + (m.get("away_corners") or 0) for m in all_finished if m.get("home_corners") is not None]
        avg_corners = round(sum(c_list) / len(c_list), 2) if c_list else 5.5
    else:
        avg_goals = 2.7
        avg_corners = 5.5

    return {
        "home_name": home_team.get("name") if home_team else "?",
        "away_name": away_team.get("name") if away_team else "?",
        "home_last5": home_last5,
        "away_last5": away_last5,
        "home_played": home_stats["played"],
        "away_played": away_stats["played"],
        "home_avg_corners": home_stats["avg_corners"],
        "away_avg_corners": away_stats["avg_corners"],
        "h2h": h2h,
        "league_avg_goals": avg_goals,
        "league_avg_corners": avg_corners,
    }


async def generate_and_save_odds(match_id: str) -> Dict[str, float]:
    m = await db.matches.find_one({"id": match_id}, {"_id": 0})
    if not m:
        raise HTTPException(404, "Maç bulunamadı")
    if m.get("status") in ("finished", "canceled"):
        # Not necessary but harmless.
        pass
    ctx = await build_odds_context(m)
    odds = await generate_odds_via_groq(ctx)
    await db.matches.update_one({"id": match_id}, {"$set": {"odds": odds, "odds_generated_at": now_iso()}})
    return odds


# ----------------------------- Coupon Settlement -----------------------------
def _selection_result(bet_type: str, selection: str, home_score: int, away_score: int,
                      home_corners: Optional[int], away_corners: Optional[int]) -> Optional[str]:
    """Return 'WON'|'LOST', or None if data insufficient (e.g. no corners)."""
    if bet_type == "MS":
        outcome = "1" if home_score > away_score else ("2" if away_score > home_score else "X")
        return "WON" if selection == outcome else "LOST"
    if bet_type == "GOAL_O_U":
        total = home_score + away_score
        over = total > 2.5  # >2.5 means >=3
        if selection == "OVER_2_5":
            return "WON" if over else "LOST"
        return "WON" if not over else "LOST"
    if bet_type == "CORNER_O_U":
        if home_corners is None or away_corners is None:
            return None
        total = home_corners + away_corners
        over = total > 4.5
        if selection == "OVER_4_5":
            return "WON" if over else "LOST"
        return "WON" if not over else "LOST"
    return None


async def _match_snapshot(match_id: str) -> Optional[Dict[str, Any]]:
    m = await db.matches.find_one(
        {"id": match_id},
        {"_id": 0, "status": 1, "home_score": 1, "away_score": 1,
         "home_corners": 1, "away_corners": 1, "home_team_id": 1, "away_team_id": 1}
    )
    return m


async def settle_coupons_for_match(match_id: str):
    """Called after admin finalizes a match. Re-evaluates all coupons touching this match."""
    coupons = await db.coupons.find({"items.match_id": match_id, "status": "PENDING"}, {"_id": 0}).to_list(5000)
    for coupon in coupons:
        await _settle_one(coupon)


async def _settle_one(coupon: dict):
    """Evaluate coupon; if all items finalized, mark WON/LOST and pay out."""
    items = coupon.get("items", [])
    new_items = []
    any_lost = False
    all_done = True
    for it in items:
        m = await _match_snapshot(it["match_id"])
        if not m or m.get("status") not in ("finished",):
            new_items.append({**it, "status": "PENDING"})
            all_done = False
            continue
        hs = int(m.get("home_score") or 0)
        as_ = int(m.get("away_score") or 0)
        hc = m.get("home_corners")
        ac = m.get("away_corners")
        res = _selection_result(it["bet_type"], it["selection"], hs, as_, hc, ac)
        if res is None:
            # Corner data missing → still pending
            new_items.append({**it, "status": "PENDING"})
            all_done = False
            continue
        new_items.append({**it, "status": res})
        if res == "LOST":
            any_lost = True

    update = {"items": new_items}
    if any_lost:
        update["status"] = "LOST"
        update["settled_at"] = now_iso()
    elif all_done:
        # All WON
        update["status"] = "WON"
        update["settled_at"] = now_iso()
        payout = round(coupon["stake"] * coupon["total_odd"])
        update["payout"] = payout
        # Credit user
        await db.users.update_one({"id": coupon["user_id"]}, {"$inc": {"points": payout}})
    else:
        update["status"] = "PENDING"

    await db.coupons.update_one({"id": coupon["id"]}, {"$set": update})

    # Push notification on final settle
    if update.get("status") in ("WON", "LOST"):
        try:
            user = await db.users.find_one({"id": coupon["user_id"]}, {"push_subscription": 1, "username": 1})
            title = "🎉 KUPONUN KAZANDI!" if update["status"] == "WON" else "💔 Kupon Kaybetti"
            body = (f"+{update.get('payout', 0)} coin • {len(new_items)} maçlı"
                    if update["status"] == "WON" else "Bir sonraki sefere.")
            await broadcast_push(title, body, "/my-coupons")
        except Exception as exc:
            logger.warning("push settle failed: %s", exc)


# ----------------------------- Match hooks (called from server.py) -----------------------------
async def on_match_finished(match_id: str, home_score: int, away_score: int,
                            home_corners: Optional[int], away_corners: Optional[int]):
    """Called when admin finishes a match. Awards goal coins + settles coupons."""
    m = await db.matches.find_one({"id": match_id}, {"_id": 0})
    if not m:
        return
    # Award +15 per goal to each team's owner (manager user), only once per match.
    if not m.get("goal_rewards_paid"):
        home = await db.teams.find_one({"id": m["home_team_id"]}, {"user_id": 1})
        away = await db.teams.find_one({"id": m["away_team_id"]}, {"user_id": 1})
        if home and home.get("user_id") and home_score > 0:
            await db.users.update_one({"id": home["user_id"]}, {"$inc": {"points": GOAL_REWARD * int(home_score)}})
        if away and away.get("user_id") and away_score > 0:
            await db.users.update_one({"id": away["user_id"]}, {"$inc": {"points": GOAL_REWARD * int(away_score)}})
        await db.matches.update_one({"id": match_id}, {"$set": {"goal_rewards_paid": True}})

    await settle_coupons_for_match(match_id)


