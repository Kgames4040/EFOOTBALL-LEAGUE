@router.post("/founder/coupons/{coupon_id}/cancel")
async def founder_cancel_coupon(coupon_id: str, admin: dict = Depends(require_founder)):
    c = await db.coupons.find_one({"id": coupon_id}, {"_id": 0})
    if not c:
        raise HTTPException(404, "Kupon bulunamadı")
    if c.get("status") == "REFUNDED":
        raise HTTPException(400, "Kupon zaten iade edildi")
    # Refund stake
    await db.users.update_one({"id": c["user_id"]}, {"$inc": {"points": int(c["stake"])}})
    # If it was WON, deduct the payout
    if c.get("status") == "WON" and c.get("payout"):
        await db.users.update_one({"id": c["user_id"]}, {"$inc": {"points": -int(c["payout"])}})
    await db.coupons.update_one({"id": coupon_id}, {"$set": {"status": "REFUNDED",
                                                              "canceled_at": now_iso(),
                                                              "canceled_by": admin["username"]}})
    try:
        await broadcast_push("↩️ Kuponun iade edildi",
                             f"{c['stake']} coin bakiyene geri yatırıldı.",
                             "/my-coupons")
    except Exception:
        pass
    return {"status": "REFUNDED"}


@router.post("/founder/coupons/{coupon_id}/set-status")
async def founder_set_coupon_status(coupon_id: str, req: CouponStatusReq, admin: dict = Depends(require_founder)):
    if req.status not in ("WON", "LOST", "PENDING"):
        raise HTTPException(400, "Geçersiz durum")
    c = await db.coupons.find_one({"id": coupon_id}, {"_id": 0})
    if not c:
        raise HTTPException(404, "Kupon bulunamadı")
    prev = c.get("status")
    payout = int(c.get("payout") or 0)
    # Reverse previous payout if any
    if prev == "WON" and payout:
        await db.users.update_one({"id": c["user_id"]}, {"$inc": {"points": -payout}})

    update = {"status": req.status, "settled_at": now_iso(), "manual_override_by": admin["username"]}
    if req.status == "WON":
        new_payout = round(c["stake"] * c["total_odd"])
        update["payout"] = new_payout
        await db.users.update_one({"id": c["user_id"]}, {"$inc": {"points": new_payout}})
    else:
        update["payout"] = 0

    await db.coupons.update_one({"id": coupon_id}, {"$set": update})
    try:
        if req.status == "WON":
            await broadcast_push("🎉 Kupon Kazandı!", f"+{update['payout']} coin", "/my-coupons")
        elif req.status == "LOST":
            await broadcast_push("💔 Kupon Kaybetti", "Kurucu manuel olarak güncelledi.", "/my-coupons")
    except Exception:
        pass
    return {"status": req.status, "payout": update.get("payout", 0)}


@router.get("/founder/stats")
async def founder_stats(admin: dict = Depends(require_founder)):
    now = datetime.now(timezone.utc)
    day_start = now.replace(hour=0, minute=0, second=0, microsecond=0).isoformat()
    today = await db.coupons.aggregate([
        {"$match": {"created_at": {"$gte": day_start}}},
        {"$group": {"_id": None,
                    "count": {"$sum": 1},
                    "staked": {"$sum": "$stake"},
                    "won_amount": {"$sum": {"$cond": [{"$eq": ["$status", "WON"]}, "$payout", 0]}}}}
    ]).to_list(1)
    all_time = await db.coupons.aggregate([
        {"$group": {"_id": None,
                    "count": {"$sum": 1},
                    "staked": {"$sum": "$stake"},
                    "won_amount": {"$sum": {"$cond": [{"$eq": ["$status", "WON"]}, "$payout", 0]}},
                    "pending": {"$sum": {"$cond": [{"$eq": ["$status", "PENDING"]}, 1, 0]}}}}
    ]).to_list(1)
    t = today[0] if today else {"count": 0, "staked": 0, "won_amount": 0}
    a = all_time[0] if all_time else {"count": 0, "staked": 0, "won_amount": 0, "pending": 0}
    # Money supply
    users = await db.users.find({}, {"_id": 0, "points": 1}).to_list(5000)
    total_points = sum(int(u.get("points") or 0) for u in users)
    return {
        "today": {"count": t.get("count", 0), "staked": t.get("staked", 0), "won": t.get("won_amount", 0)},
        "all_time": {"count": a.get("count", 0), "staked": a.get("staked", 0),
                     "won": a.get("won_amount", 0), "pending": a.get("pending", 0)},
        "total_points_in_circulation": total_points,
    }


