@router.post("/coupons")
async def create_coupon(req: CouponCreateReq, user: dict = Depends(get_current_user)):
    if req.stake < MIN_STAKE:
        raise HTTPException(400, f"Minimum bahis {MIN_STAKE} coin")
    if not req.items:
        raise HTTPException(400, "En az bir seçim ekleyin")

    seen_matches = set()
    validated = []
    total_odd = 1.0
    for it in req.items:
        if it.match_id in seen_matches:
            raise HTTPException(400, "Bir maça yalnızca bir bahis eklenebilir")
        seen_matches.add(it.match_id)
        if it.bet_type not in BET_TYPES:
            raise HTTPException(400, f"Geçersiz bahis türü: {it.bet_type}")
        if it.selection not in SELECTIONS[it.bet_type]:
            raise HTTPException(400, f"Geçersiz seçim: {it.selection}")

        m = await db.matches.find_one({"id": it.match_id}, {"_id": 0})
        if not m:
            raise HTTPException(404, f"Maç bulunamadı: {it.match_id}")
        if m.get("status") != "scheduled":
            raise HTTPException(400, "Sadece programlanmış maçlara bahis oynanabilir")
        locks = m.get("bet_locks") or {}
        if locks.get(it.bet_type):
            raise HTTPException(400, f"Bu maçta {it.bet_type} bahsi kapatıldı")
        odds = m.get("odds") or {}
        odds_key = ODDS_KEYS[it.bet_type][it.selection]
        if odds_key not in odds:
            raise HTTPException(400, "Bu maç için oranlar henüz oluşturulmadı")
        odd = float(odds[odds_key])
        total_odd *= odd
        validated.append({
            "match_id": it.match_id,
            "bet_type": it.bet_type,
            "selection": it.selection,
            "odd": round(odd, 2),
            "status": "PENDING",
        })

    total_odd = round(total_odd, 2)
    potential = round(req.stake * total_odd)

    # Deduct points atomically
    pts = await ensure_user_points(user["id"])
    if pts < req.stake:
        raise HTTPException(400, "Yetersiz bakiye")
    upd = await db.users.update_one(
        {"id": user["id"], "points": {"$gte": req.stake}},
        {"$inc": {"points": -req.stake}}
    )
    if upd.modified_count != 1:
        raise HTTPException(400, "Bakiye yetersiz veya işlem başarısız")

    doc = {
        "id": new_id(),
        "user_id": user["id"],
        "username": user["username"],
        "items": validated,
        "stake": req.stake,
        "total_odd": total_odd,
        "potential_payout": potential,
        "status": "PENDING",
        "created_at": now_iso(),
    }
    await db.coupons.insert_one(dict(doc))
    return doc


